def _get_test_complex_1():
    st = SimplexTree()
    st.insert([0], 1.0)
    st.insert([1], 2.0)
    st.insert([2], 1.0)
    st.insert([0, 1], 6.0)
    st.insert([1, 2], 4.0)
    st.insert([0, 2], 3.0)
    st.insert([0, 1, 2], np.inf)
    st.insert([3], np.inf)
    st.insert([1, 3], np.inf)
    return st


def _update_test_complex_1_step_1(st):
    st.assign_filtration([0], 1.0)
    st.assign_filtration([1], 1.0)
    st.assign_filtration([2], 2.0)
    st.assign_filtration([0, 1], 3.0)
    st.assign_filtration([1, 2], 4.0)
    st.assign_filtration([0, 2], 6.0)
    st.assign_filtration([0, 1, 2], 6.0)
    st.assign_filtration([3], 7.0)
    st.assign_filtration([1, 3], 7.0)
    return st


def _update_test_complex_1_step_2(st):
    st.assign_filtration([0], 0.0)
    st.assign_filtration([1], -1.0)
    st.assign_filtration([2], 1.0)
    st.assign_filtration([0, 1], 1.0)
    st.assign_filtration([1, 2], 3.0)
    st.assign_filtration([0, 2], 4.0)
    st.assign_filtration([0, 1, 2], 4.0)
    st.assign_filtration([3], 6.0)
    st.assign_filtration([1, 3], 6.0)
    return st


def _get_test_complex_2():
    bc = [
        np.array([]),
        np.array([]),
        np.array([]),
        np.array([0, 1]),
        np.array([1, 2]),
        np.array([0, 2]),
        np.array([3, 4, 5]),
        np.array([]),
        np.array([1, 7]),
    ]
    dc = np.array([0, 0, 0, 1, 1, 1, 2, 0, 1])
    fc = np.array([1.0, 2.0, 1.0, 6.0, 4.0, 3.0, np.inf, np.inf, np.inf])
    return (bc, dc, fc)


def _update_test_complex_2_step_1():
    return np.array([1.0, 1.0, 2.0, 3.0, 4.0, 6.0, 6.0, 7.0, 7.0])


def _update_test_complex_2_step_2():
    return np.array([0.0, -1.0, 1.0, 1.0, 3.0, 4.0, 4.0, 6.0, 6.0])


def test_vineyard():
    pers = [None] * 3
    vy1 = Vineyard()
    cpx = _get_test_complex_1()
    pers[0] = cpx.persistence(homology_coeff_field=2, min_persistence=-1)
    pers[0] = [bar[1] for bar in pers[0] if bar[0] == 1]
    vy1.initialize(filtered_cpx=cpx, number_of_updates=2)
    cpx = _update_test_complex_1_step_1(cpx)
    pers[1] = cpx.persistence(homology_coeff_field=2, min_persistence=-1)
    pers[1] = [bar[1] for bar in pers[1] if bar[0] == 1]
    vy1.update(filtered_cpx=cpx)
    cpx = _update_test_complex_1_step_2(cpx)
    pers[2] = cpx.persistence(homology_coeff_field=2, min_persistence=-1)
    pers[2] = [bar[1] for bar in pers[2] if bar[0] == 1]
    vy1.update(filtered_cpx=cpx)
    vineyard1 = vy1.get_current_vineyard_view()

    np.testing.assert_equal(vineyard1, vy1.get_current_vineyard())

    for i in range(3):
        assert len(vineyard1) != 1 or len(pers[i]) == 0

        v_pers = [(b, d) for b, d in vineyard1[1][:, i, :]]
        assert len(pers[i]) == len(v_pers)

        pers[i].sort()
        v_pers.sort()
        assert pers[i] == v_pers

    vy2 = Vineyard()
    cpx = _get_test_complex_2()
    vy2.initialize(
        boundaries=cpx[0], dimensions=cpx[1], filtration_values=cpx[2], number_of_updates=2
    )
    cpx = _update_test_complex_2_step_1()
    vy2.update(filtration_values=cpx)
    cpx = _update_test_complex_2_step_2()
    vy2.update(filtration_values=cpx)
    vineyard2 = vy2.get_current_vineyard_view()

    np.testing.assert_equal(vineyard2, vy2.get_current_vineyard())
    np.testing.assert_equal(vineyard2[1], vy2.get_current_vineyard(dim=1))
    np.testing.assert_equal(vineyard1, vineyard2)

    vineyard2 = vy2.get_current_vineyard(dim=1, min_bar_length=1.0)
    diff = (vineyard2[:, :, 1] - vineyard2[:, :, 0]) >= 1.0
    # at least one coordinate of the vines are >= 1.0
    assert np.all(np.any(diff, axis=1))

    cpx = _get_test_complex_1()
    pers[0] = cpx.persistence(homology_coeff_field=2, min_persistence=1.0)
    pers[0] = [bar[1] for bar in pers[0] if bar[0] == 1]
    cpx = _update_test_complex_1_step_1(cpx)
    pers[1] = cpx.persistence(homology_coeff_field=2, min_persistence=1.0)
    pers[1] = [bar[1] for bar in pers[1] if bar[0] == 1]
    cpx = _update_test_complex_1_step_2(cpx)
    pers[2] = cpx.persistence(homology_coeff_field=2, min_persistence=1.0)
    pers[2] = [bar[1] for bar in pers[2] if bar[0] == 1]

    for i in range(3):
        v_pers = [(b, d) for b, d in vineyard2[:, i, :] if (d - b) >= 1.0]
        assert len(pers[i]) == len(v_pers)

        pers[i].sort()
        v_pers.sort()
        assert pers[i] == v_pers


